    public void update(){
        
        map = new TreeMap<String,Order>();
        
        
        
        listModel = new DefaultListModel<String>();
        
        
        
        int index = 0;
        for(Order o : dh.getOrders()){
            index ++;
            map.put("" + o.getOrderNumber(), o);
            listModel.addElement("" + o.getOrderNumber());
        }
        
        
        jList1.setModel(listModel);
        jList1.setSelectedIndex(index-1);
        
        
        
        int height = jPanel7.getSize().height;
        
        
        
        int a = dh.getOrders().size() * 20;
        System.out.println("a = " + a);
        
        
        jScrollPane1.setPreferredSize(new Dimension(30, Math.min(Math.max(26, a+6), height)));
       
        
        this.revalidate();
        this.repaint();
    }
    
