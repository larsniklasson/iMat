    public void update(){
        
        map = new TreeMap<String,String>();
        
        int index = jList1.getSelectedIndex();
        if(index == -1){
            index = 0;
        }
        
        listModel = new DefaultListModel<String>();
        
        File folder = new File(dh.imatDirectory() + "/ink√∂pslistor");
        
        File[] listOfFiles = folder.listFiles();
        
        if(listOfFiles.length == 0){
            jPanel1.removeAll();
            jPanel1.revalidate();
            jPanel1.repaint();
            
            jLabel3.setText("Totalt: ");
            jLabel2.setText("");
        }
        
        Scanner sc = null;
        for(File f : listOfFiles){
            try {
                sc = new Scanner(f);
            } catch (FileNotFoundException ex) {
                System.out.println("catch block");
                Logger.getLogger(ListorPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            String listName = sc.nextLine().split(";")[0];
            sc.close();
            listModel.addElement(listName);
            map.put(listName, f.getName());
        }
        
        
        jList1.setModel(listModel);
        jList1.setSelectedIndex(index);
        //this.repaint();
    }
    
